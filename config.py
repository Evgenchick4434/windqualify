def get_city_ru(city):
    if city is None:
        return 'not defined'
    r = requests.get(
        f'https://translate.googleapis.com/translate_a/single?client=gtx&sl=auto&tl=ru&dt=t&q={city}'
    )
    data = r.json()
    city_ru = data[0][0][0]
    return city_ru
